class Users {

  final String id;
  final String name;
  final String surName;
  final String userName;
  final String email;
  final DateTime birthDate;
  final String url;
  final String about;

  Users({
    required this.id,
    required this.name,
    required this.surName,
    required this.userName,
    required this.email,
    required this.birthDate,
    required this.url,
    required this.about
  });

}